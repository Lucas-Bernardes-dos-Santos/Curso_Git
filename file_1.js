const calcularDivisao = (_numA, _numB) => _numA / _numB
const calcularMultiplicacao = (_numA, _numB) => _numA * _numB
const calcularSoma = (_numA, _numB) => _numA + _numB
const calcularSubtracao  = (_numA, _numB) => _numA - _numB

console.log(`A divisão entre ${7} e ${4} é igual a ${calcularDivisao(7, 4)}`)
console.log(`A multiplicação entre ${3} e ${6} é igual a ${calcularMultiplicacao(3, 6)}`)
console.log(`A soma entre ${7} e ${9} é igual a ${calcularSoma(7, 9)}`)
console.log(`A subtração entre ${4} e ${7} é igual a ${calcularSubtracao(4, 7)}`)
