const calcularDivisao = (_numA, _numB) => _numA / _numB
const calcularSoma = (_numA, _numB) => _numA + _numB
const calcularSubtracao  = (_numA, _numB) => _numA - _numB

console.log(calcularDivisao(7, 4))
console.log(calcularSoma(7, 9));
console.log(`A subtração entre ${4} e ${7} é igual a ${calcularSubtracao(4, 7)}`)